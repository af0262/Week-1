#This program calculates the temperature in Celsius when Fahrenheit inputted

def main():

    print ("This Program Provides The Temp in Celsius When The Fahrenheit is inputted")

    C = float(input("Please Enter The Temp in Celsius: "))
    F = (C-32)/1.8

    print("The Temp in Celsius is: ", F)


main()
